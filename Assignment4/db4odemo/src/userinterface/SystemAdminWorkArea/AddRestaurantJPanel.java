    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        String userName = userNameText.getText();
        String password = passwordText.getText();
        String repeatPassword = repeatPasswordText.getText();
        String restaurantName = restaurantNameText.getText();
        String phone = phoneText.getText();
        String address = addressText.getText();
        String managerName = managerNameText.getText();
        String dish1 = dish1Text.getText();
        String dish2 = dish2Text.getText();
        String dish3 = dish3Text.getText();
        

        if(userName.isEmpty() || password.isEmpty() || repeatPassword.isEmpty() || restaurantName.isEmpty() || phone.isEmpty() || address.isEmpty() || managerName.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please input all information, some are empty!");
        }
        else if(!system.getUserAccountDirectory().checkIfUsernameIsUnique(userName)){
            JOptionPane.showMessageDialog(null, "User Name already exist!");
        }
        else if(!restaurantDirectory.isPhoneUnique(phone)){
            JOptionPane.showMessageDialog(null, "This phone number already exist!");
        }
        else if(!password.equals(repeatPassword)){
            JOptionPane.showMessageDialog(null, "Two password input are not equal!");
        }
        else{
            Restaurant restaurant = new Restaurant(restaurantName,phone,address, managerName);
            restaurant.setRestaurantID("R" + (restaurantDirectory.getRestaurants().size()+1));
            restaurant.getMenu().add(dish1);
            restaurant.getMenu().add(dish2);
            restaurant.getMenu().add(dish3);
            restaurantDirectory.getRestaurants().add(restaurant);
            Employee employee = system.getEmployeeDirectory().createEmployee(restaurant.getRestaurantID());

            UserAccount account = system.getUserAccountDirectory().createUserAccount(userName, password, employee, new AdminRole());
            JOptionPane.showMessageDialog(null, "Restaurant added!");
            restaurantNameText.setText("");
            passwordText.setText("");
            repeatPasswordText.setText("");
            restaurantNameText.setText("");
            phoneText.setText("");
            addressText.setText("");
            managerNameText.setText("");
            dish1Text.setText("");
            dish2Text.setText("");
            dish3Text.setText("");
        }
    }//GEN-LAST:event_AddBtnActionPerformed

